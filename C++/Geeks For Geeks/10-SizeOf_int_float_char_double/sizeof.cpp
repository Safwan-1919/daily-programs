#include <iostream>
using namespace std;

int main() {
    int a = 10;
    float b = 2.5;
    char c = 'a';
    double d = 3.997;
    bool e = true;
    cout<<"Int: "<<sizeof(a)<<endl;
    cout<<"Float: "<<sizeof(b)<<endl;
    cout<<"Char: "<<sizeof(c)<<endl;
    cout<<"Double: "<<sizeof(d)<<endl;
    cout<<"Bool: "<<sizeof(e)<<endl;
    return 0;
}
